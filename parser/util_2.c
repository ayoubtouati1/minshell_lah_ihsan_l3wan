#include "minishell.h"

void	add_befor_opirations(char **str, char **dest, int *i, int *j)
{
	if (((*str)[*i + 1] == '<' && (*str)[*i] != '<')
		|| ((*str)[*i + 1] == '>' && (*str)[*i] != '>')
		|| ((*str)[*i + 1] == '|' && (*str)[*i] != '|'))
	{
		(*j)++;
		(*dest)[*j] = ' ';
	}
}